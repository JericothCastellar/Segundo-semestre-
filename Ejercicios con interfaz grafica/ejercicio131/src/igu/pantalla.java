    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String numero1 = texto1.getText();
String numero2 = texto2.getText();
String numero3 = texto3.getText();
int nombre = Integer.parseInt(numero1);
int horasTrabajadas = Integer.parseInt(numero2);
int tarifaPorHora = Integer.parseInt(numero3);
double salario;
        if (horasTrabajadas <= 40) {
            salario = horasTrabajadas * tarifaPorHora;
        } else {
            double horasRegulares = 40;
            double horasExtras = horasTrabajadas - 40;
            salario = (horasRegulares * tarifaPorHora) + (horasExtras * (tarifaPorHora * 2));
        }

        JOptionPane.showMessageDialog(null,"Nombre del trabajador: " + nombre);
        JOptionPane.showMessageDialog(null,"Salario total: $" + salario);
    }//GEN-LAST:event_jButton1ActionPerformed

