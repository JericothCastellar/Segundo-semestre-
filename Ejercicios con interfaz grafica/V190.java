     a.setText("");
        if(i<=5){
        if((i+1)<6){
         T.setText("TRANSACCION " + (i+1));
        }else{
           T.setText("TRANSACCION " + i); 
        }
         int m=Integer.parseInt(MP.getText());
         int c=Integer.parseInt(PC.getText());
        int diferencia=c-m;
        if(diferencia<0){
            a.setText("<html>El cliente no ha pagado lo suficiente. Faltan $" + Math.abs(diferencia)+"<html>");
        }else{
            b="La diferencia es de " + diferencia+"\n";
           for (int denominacion : denominaciones) {
                    int cantidadDenominacion = diferencia / denominacion;
                    diferencia %= denominacion;

                    if (cantidadDenominacion > 0) {
                       b+=("Denominaci√≥n $" + denominacion + ": " + cantidadDenominacion + " billetes\n");
                    }
                }
          a.setText("<html>"+b+"<html>");
        
        }
        
     }
     i++;
