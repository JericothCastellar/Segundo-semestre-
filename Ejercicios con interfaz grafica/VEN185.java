    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (etiNombre.getText().equals("") || etiApellido.getText().equals("") || eti2Apellido.getText().equals("")
                || cajDia.getSelectedItem().equals("DIA") || cajMes.getSelectedItem().equals("Mes")
                || cajAño.getSelectedItem().equals("Año")) {
            JOptionPane.showMessageDialog(null, "RELLENE TODOS LOS CAMPOS");

        } else {
            String apellido2 = eti2Apellido.getText();
            char a = apellido2.charAt(0);
            String nombre = etiNombre.getText();
            char b = nombre.charAt(0);
            String apellido = etiApellido.getText();
            char c = apellido.charAt(0);
            String n2 = "" + a + b;
            String ncompleto = "" + a + b + c;
            String Dia = (String) cajDia.getSelectedItem();
            String Mes = (String) cajMes.getSelectedItem();
            String Año = (String) cajAño.getSelectedItem();
            char ale;
            int Añito = Integer.parseInt(Año);
            if (Añito > 2000) {
                ale = (char) (Math.random() * 26 + 'A');
            } else {
                ale = (char) ((char) (Math.random() * 9) + 1);

            }
            int ue = Añito % 100;

            String Nacimiento = "";
            if (cajExtranjero.getSelectedItem().equals("Si")) {
                Nacimiento = "NE";
            } else if (cajExtranjero.getSelectedItem().equals("No")) {
                Nacimiento = "NN";
            }
            String Sexo = (String) cajSex.getSelectedItem();
            String m = "";
            if (Mes.equals("Enero")) {
                m = "01";
            }
            if (Mes.equals("Febrero")) {
                m = "02";
            }
            if (Mes.equals("Marzo")) {
                m = "03";
            }
            if (Mes.equals("Abril")) {
                m = "04";
            }
            if (Mes.equals("Mayo")) {
                m = "05";
            }
            if (Mes.equals("Junio")) {
                m = "06";
            }
            if (Mes.equals("Julio")) {
                m = "07";
            }
            if (Mes.equals("Agosto")) {
                m = "08";
            }
            if (Mes.equals("Septiembre")) {
                m = "09";
            }
            if (Mes.equals("Octubre")) {
                m = "10";
            }
            if (Mes.equals("Noviembre")) {
                m = "11";
            }
            if (Mes.equals("Diciembre")) {
                m = "12";
            }
            String Homoclave = JOptionPane.showInputDialog(null, "DIJITE LA HOMOCLAVE");
            etiRFC.setText(n2 + ue + m + Dia + Sexo + Nacimiento + ncompleto + ale+Homoclave);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

