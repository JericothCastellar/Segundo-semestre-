    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String c1 = campo1.getText();
        double valor1 = Double.parseDouble(c1);

        String c2 = campo2.getText();
        double valor2 = Double.parseDouble(c2);

        double suma = valor1 + valor2;
        double resta = valor1 - valor2;
        
        sum.setText(suma+"");
        rest.setText(resta+"");

        if (resta != 0 && suma % resta == 0) {
            Res.setText("La división es exacta.");
        } else {
            Res.setText("La división no es exacta.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

