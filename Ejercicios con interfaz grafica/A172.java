    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String c1 = mes.getText();
        int Mes = Integer.parseInt(c1);
        
        String c2 = año.getText();
        int Año = Integer.parseInt(c2);
        
        Calendar calendar = Calendar.getInstance();
        calendar.set(Calendar.YEAR, Año);
        calendar.set(Calendar.MONTH, Mes - 1);
        
        SimpleDateFormat formatoSalida = new SimpleDateFormat("MM-yyyy");
        StringBuilder resultado = new StringBuilder();
            
        for (int i = 0; i < 12; i++) {
                // Muestra el mes en el formato "MM-yyyy" utilizando la variable calendar.
                resultado.append(formatoSalida.format(calendar.getTime())+"  ");
                
                // Resta un mes al calendario para obtener el mes anterior.
                calendar.add(Calendar.MONTH, -1);
                
                
            }
        String resultadoFinal = resultado.toString();
        
        Resultado.setText(resultadoFinal+"");
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

