    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double totalFactura = 0;
        int cantidadTotalProductos = 0;

        while (true) {
            int cantidad =Integer.parseInt(JOptionPane.showInputDialog("Introduzca la cantidad vendida (0 para finalizar): "));
            

            if (cantidad == 0) {
                break;
            }

            double precio = Double.parseDouble(JOptionPane.showInputDialog("Introduzca el precio: "));
            

            double subtotal = cantidad * precio;
            totalFactura += subtotal;
            cantidadTotalProductos += cantidad;
        }

        pagar.setText(totalFactura+"");
        prod.setText(cantidadTotalProductos+"");

    }//GEN-LAST:event_jButton1ActionPerformed

