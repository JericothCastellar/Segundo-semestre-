    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Scanner scanner = new Scanner(System.in);
        String numero1 = texto1.getText();
        String numero2 = texto2.getText();
        int opcion = Integer.parseInt(numero1);
        int saldo = Integer.parseInt(numero2);
        switch (opcion) {
                case 1:
                    JOptionPane.showMessageDialog(null,"Ingrese la cantidad a depositar: ");
                    double cantidadDeposito = scanner.nextDouble();
                    saldo += cantidadDeposito;
                    JOptionPane.showMessageDialog(null,"Depósito exitoso. Saldo actual: $" + saldo);
                    break;
                case 2:
                    JOptionPane.showMessageDialog(null,"Ingrese la cantidad a retirar: ");
                    double cantidadRetiro = scanner.nextDouble();
                    if (cantidadRetiro > saldo) {
                        JOptionPane.showMessageDialog(null,"Saldo insuficiente. Saldo actual: $" + saldo);
                    } else {
                        saldo -= cantidadRetiro;
                        JOptionPane.showMessageDialog(null,"Retiro exitoso. Saldo actual: $" + saldo);
                    }
                    break;
                case 3:
                    JOptionPane.showMessageDialog(null,"Saldo final: $" + saldo);
                    JOptionPane.showMessageDialog(null,"¡Gracias por usar el cajero automático!");
                    System.exit(0);
                    break;
                default:
                    JOptionPane.showMessageDialog(null,"Opción no válida. Por favor, elija una opción válida.");
            }
    }//GEN-LAST:event_jButton1ActionPerformed

