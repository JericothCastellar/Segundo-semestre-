    private void CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularActionPerformed
       
        double temp[] = new double[6];
        
        temp[0]= Double.parseDouble(txt1.getText());
        temp[1]= Double.parseDouble(txt2.getText());
        temp[2]= Double.parseDouble(txt3.getText());
        temp[3]= Double.parseDouble(txt4.getText());
        temp[4]= Double.parseDouble(txt5.getText());
        temp[5]= Double.parseDouble(txt6.getText());
        
        double acum=0;
        double mayor=0;
        double menor=12;
        for (int i = 0; i < 6; i++) {
            acum+=temp[i];
            if(temp[i]>mayor){
                mayor=temp[i];
            }
            if(temp[i]<menor){
                menor=temp[i];
            }
            
        }
        double prom=acum/6;
        tempMayor.setText("Temperatura mayor es: "+mayor);
        tempMenor.setText("Temperatura menor es: "+menor);
        media.setText("La temperatura media del dia fue: "+ prom );
    }//GEN-LAST:event_CalcularActionPerformed

