    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String departamento = c2.getText().trim().toLowerCase();
        String nombreCompleto = c1.getText().trim().toLowerCase();
        
        String[] partesNombre = nombreCompleto.split(" ");
        
        if (partesNombre.length >= 3){
            String apellido1 = partesNombre[0];
            String nombre = partesNombre[1];
            String apellido2 = partesNombre[2];
            
            String nombreUsuario = departamento.substring(0, 2) + nombre.charAt(0) + nombre.charAt(nombre.length() - 1) + apellido1 + apellido2.charAt(0) + apellido2.charAt(apellido2.length() - 1);

        
        Usr.setText(nombreUsuario);
    } else {
        JOptionPane.showMessageDialog(this, "Ingrese un nombre completo v√°lido con al menos un nombre y dos apellidos.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

