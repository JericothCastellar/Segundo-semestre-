        String a="";
        int datos [] = new int [10];
        datos[0]=Integer.parseInt(n1.getText());
        datos[1]=Integer.parseInt(n2.getText());
        datos[2]=Integer.parseInt(n3.getText());
        datos[3]=Integer.parseInt(n4.getText());
        datos[4]=Integer.parseInt(n5.getText());
        datos[5]=Integer.parseInt(n6.getText());
        datos[6]=Integer.parseInt(n7.getText());
        datos[7]=Integer.parseInt(n8.getText());
        datos[8]=Integer.parseInt(n9.getText());
        datos[9]=Integer.parseInt(n10.getText());
         int n = datos.length;
        boolean intercambio;

        do {
            intercambio = false;
            for (int i = 1; i < n; i++) {
                if (datos[i - 1] > datos[i]) {
                    // Intercambiar elementos si est√°n en el orden incorrecto
                    int temp = datos[i - 1];
                    datos[i - 1] = datos[i];
                    datos[i] = temp;
                    intercambio = true;
                }
            }
        } while (intercambio);
        for(int j=0; j<datos.length;j++){
            a+=","+ datos[j];
            }
        r.setText(a);
