    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int n = Integer.parseInt(entrada.getText());
        
        ArrayList<Integer> factoresPrimos = new ArrayList<>();
        
        for (int i = 2; i * i <= n; i++) {

            if (n % i == 0) {

                factoresPrimos.add(i);

                n /= i;
            }
        }

        if (n > 1) {
            factoresPrimos.add(n);
        }

        
        for (int i = 0; i < factoresPrimos.size(); i++) {
            salida.setText("La descomposiciÃ³n en factores primos de " + n + " es: " + factoresPrimos.get(i) + " ");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

